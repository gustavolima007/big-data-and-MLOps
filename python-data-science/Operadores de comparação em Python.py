# Operadores de comparação em Python

# Operador   | Operação
# -----------------------------------------------
# ==         | True se os valores em ambos lados forem iguais
# !=         | True se os valores em ambos lados forem diferentes
# <>         | True se os valores em ambos lados forem diferentes (OBS: Esse operador é obsoleto, use "!=")
# >          | True se o valor da esquerda for maior que o valor da direita
# <          | True se o valor da esquerda for menor que o valor da direita
# >=         | True se o valor da esquerda for maior ou igual ao valor da direita
# <=         | True se o valor da esquerda for menor ou igual ao valor da direita